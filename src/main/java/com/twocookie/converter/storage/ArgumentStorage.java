package com.twocookie.converter.storage;

public class ArgumentStorage {

  private InputStorage inputStorage;
  private OutputStorage outputStorage;

  public InputStorage getInputStorage() {
    return inputStorage;
  }

  public void setInputStorage(InputStorage input) {
    this.inputStorage = input;
  }

  public OutputStorage getOutputStorage() {
    return outputStorage;
  }

  public void setOutputStorage(OutputStorage output) {
    this.outputStorage = output;
  }
}
